#Arithmatic Operators
print(5 + 2)
print(5 - 2)
print(5 * 2)
print(5 / 2)
print(5 % 2)

print(5 // 2)      #for integer value output
print(5 ** 2)      #for calculating power

#Some Shortcuts
i = 5
i = i + 2
print(i)
i += 2
print(i)
i -= 2
print(i)
i *= 2
print(i)
i /= 2
print(i)


#Operator Precedence
result = 2 + 3 * 5
print(result)

#Comparison Operator
print(3 > 2)
print(3 < 2)
print(3 >= 2)
print(3 == 3)
print(3 != 2)

#Logical Operator
print( 3 > 2 or 2 > 1)
print( 3 > 2 and 2 > 1)
print( not 3 > 2)
